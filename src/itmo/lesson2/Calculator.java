package itmo.lesson2;

public class Calculator {
    /**Overload addition method**/
    void addition(int firstNUM, int secondNUM) {
        int result;
        result = firstNUM + secondNUM;
        System.out.println("Int result:"+result);
    }
    void addition(double firstNUM, double secondNUM) {
        double result;
        result = firstNUM + secondNUM;
        System.out.println("Double result:"+result);
    }
    void addition(long firstNUM, long secondNUM) {
        long result;
        result = firstNUM + secondNUM;
        System.out.println("Long result:"+result);
    }
    /**Overload subtraction method**/
    void subtraction(int firstNUM, int secondNUM) {
        int result;
        result = firstNUM - secondNUM;
        System.out.println("Int result:"+result);
    }
    void subtraction(double firstNUM, double secondNUM) {
        double result;
        result = firstNUM - secondNUM;
        System.out.println("Double result:"+result);
    }
    void subtraction(long firstNUM, long secondNUM) {
        long result;
        result = firstNUM - secondNUM;
        System.out.println("Long result:"+result);
    }
    /**Overload multiplication method**/
    void multiplication(int firstNUM, int secondNUM) {
        int result;
        result = firstNUM * secondNUM;
        System.out.println("Int result:"+result);
    }
    void multiplication(double firstNUM, double secondNUM) {
        double result;
        result = firstNUM * secondNUM;
        System.out.println("Double result:"+result);
    }
    void multiplication(long firstNUM, long secondNUM) {
        long result;
        result = firstNUM * secondNUM;
        System.out.println("Long result:"+result);
    }
    /**Overload division method**/
    void division(int firstNUM, int secondNUM) {
        int result;
        result = firstNUM / secondNUM;
        System.out.println("Int result:"+result);
    }
    void division(double firstNUM, double secondNUM) {
        double result;
        result = firstNUM / secondNUM;
        System.out.println("Double result:"+result);
    }
    void division(long firstNUM, long secondNUM) {
        long result;
        result = firstNUM / secondNUM;
        System.out.println("Long result:"+result);
    }
}



